var $toggle = false;
$('#q2').on("click", "li", function(ev)
{
	if ($toggle == false)
	{
		document.getElementById('q2').style.backgroundColor='yellow';
		$toggle = true
	}
	else
	{
		document.getElementById('q2').style.backgroundColor='white';
		$toggle = false;
	}
});
//--------------------------------------------------------------------------------------
	
